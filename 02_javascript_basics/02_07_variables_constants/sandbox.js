// let keyword let us create a variable

let age = 25;
let year = 2019;

console.log(age, year)

// if the variable was already declared we can reassigned the value
age = 30;
console.log(age);

// if we do not want the value of the variable to change we need a constant

const points = 100;
console.log(points);

// old way to declare variables
var score = 75;
console.log(score)

